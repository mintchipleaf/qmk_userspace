void post_process_record_user(uint16_t keycode, keyrecord_t *record) {
    switch (keycode) {
        /* KEYBOARD PET STATUS START */

        case KC_LCTL:
        case KC_RCTL:
            if (record->event.pressed) {
                isSneaking = true;
            } else {
                isSneaking = false;
            }
            break;
        case KC_SPC:
            if (record->event.pressed) {
                isJumping = true;
                showedJump = false;
            } else {
                isJumping = false;
            }
            break;

        if(get_mods() & MOD_MASK_CTRL)
        {
            isSneaking = true;
        }

        /* KEYBOARD PET STATUS END */
    }

    oled_on();
    anim_sleep = timer_read32();
}

